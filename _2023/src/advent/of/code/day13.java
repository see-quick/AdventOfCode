    private static char[][] parsePattern(String pattern) {
        String[] rows = pattern.split("\n");
        char[][] grid = new char[rows.length][rows[0].length()];

        for (int i = 0; i < rows.length; i++) {
            grid[i] = rows[i].toCharArray();
        }

        return grid;
    }

    private static int findReflectionSummary(char[][] grid) {
        for (int col = 0; col < grid[0].length / 2; col++) {
            if (isVerticalReflection(grid, col)) {
                return col; // For vertical reflection
            }
        }

        for (int row = 0; row < grid.length / 2; row++) {
            if (isHorizontalReflection(grid, row)) {
                return 100 * row; // For horizontal reflection
            }
        }

        return 0;
    }

    private static boolean isVerticalReflection(char[][] grid, int col) {
        for (int row = 0; row < grid.length; row++) {
            if (grid[row][col] != grid[row][grid[0].length - 1 - col]) {
                return false;
            }
        }
        return true;
    }

    private static boolean isHorizontalReflection(char[][] grid, int row) {
        for (int col = 0; col < grid[0].length; col++) {
            if (grid[row][col] != grid[grid.length - 1 - row][col]) {
                return false;
            }
        }
        return true;
    }
